package com.dx.admindx;

public class Post2 {

    private String user ;

    private String password ;

    private int vali ;

    public Post2(String user, String password, int vali) {
        this.user = user;
        this.password = password;
        this.vali = vali;
    }

    public String getUser() {
        return user;
    }

    public String getPassword() {
        return password;
    }

    public int getVali() {
        return vali;
    }
}
